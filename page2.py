    st.markdown("## 不易流行の意味")
    st.markdown("""
    - **不易**: 変わらないもの、普遍の真理や本質を指します。松尾芭蕉は、俳句の基本や自然の美しさ、俳諧の伝統を大切にすることが重要であると考えました。
    - **流行**: 移り変わるもの、新しい流れや変化を意味します。時代に応じて新しい価値や表現を取り入れることで、俳句が常に新鮮であり続けることができるとしました。
    """)

    st.markdown("## 松尾芭蕉の考え")
    st.markdown("""
    芭蕉は、俳句を作る際に「不易」と「流行」のバランスを重視しました。もし「不易」だけに固執すれば、俳句は古臭くなり、新しい時代に合わなくなります。しかし、「流行」だけを追いかけると、本来の俳句の本質や深みが失われてしまいます。そのため、両者を調和させることで、伝統の中にも新しさを見出し、時代に応じた価値を生み出すことが重要だと考えました。
    """)

    st.markdown("## 現代への応用")
    st.markdown("""
    この「不易流行」の考え方は、現代のビジネスや文化、芸術、技術の発展においても大切な教訓です。たとえば、企業が伝統的な価値観や理念を守りつつも、時代に合った新しい技術やサービスを取り入れることで持続可能な成長を目指す姿勢に通じています。また、個人のキャリアや自己成長においても、基本や本質を大切にしながら、新しい挑戦や学びを続けることで、自分を高めることができます。
    """)

